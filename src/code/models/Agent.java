package code.models;

public class Agent {

	private String pseudo;
	private String adresse;
	
	public Agent(String pseudo, String adresse) {
		this.pseudo = pseudo;
		this.adresse = adresse;
	}
	
	public String getPseudo() {
		return this.pseudo;
	}
	
	public String getAdresse() {
		return this.pseudo;
	}
	
	
}
